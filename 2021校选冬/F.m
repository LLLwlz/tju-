function S=F(x,y)
    S=zeros(1,5);
    if x<=y(1,1)
        if y(1,1)==y(1,2)
            S(1,1)=0.5;
            S(1,2)=0.5;
        else
            S(1,1)=1;
        end
    elseif x<y(1,2)
        S(1,1)=(y(1,2)-x)/(y(1,2)-y(1,1));
        S(1,2)=(x-y(1,1))/(y(1,2)-y(1,1));
    elseif x<y(1,3)
        S(1,2)=(y(1,3)-x)/(y(1,3)-y(1,2));
        S(1,3)=(x-y(1,2))/(y(1,3)-y(1,2));
    elseif x<y(1,4)
        S(1,3)=(y(1,4)-x)/(y(1,4)-y(1,3));
        S(1,4)=(x-y(1,3))/(y(1,4)-y(1,3));
    elseif x<y(1,5)
        S(1,4)=(y(1,5)-x)/(y(1,5)-y(1,4));
        S(1,5)=(x-y(1,4))/(y(1,5)-y(1,4));
    else
        S(1,5)=1;
    end